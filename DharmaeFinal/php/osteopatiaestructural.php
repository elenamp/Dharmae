                    sabemos que cuando se trata de niños tan pequeños, suele dar miedo, 
                    pero realmente es una maniobra muy sencilla e indolora.
                    <br><br>

                    <h3><strong>OSTEOPATÍA GINECOLÓGICA-MATERNAL:</strong></h3>
                    Si estás pensando en quedarte embarazada o si llevas tiempo
                    intentando quedarte embarazada y no lo consigues, 
                    tal vez sea el momento de que nos consultes. 
                    <br>
                    La diferencia entre consultarnos o no, está en un parto natural o una cesárea, 
                    en quedarte embarazada enseguida o tardar años. 
                    El cuerpo es muy inteligente, y jamás albergará un bebé en él si la estructura 
                    que lo va a contener (útero) y sus alrededores (pelvis y sacro) no están en condiciones. 
                    <br>
                    ¿Qué lesiones podemos encontrar que nos dificulten esto?
                    Es muy frecuente encontrar lateralizaciones de útero (úteros que no están bien colocados, 
                    úteros que están ‘tumbados’ hacia atrás…), problemas de sacro cuyo movimiento hacia atrás 
                    y hacia adelante es fundamental a la hora de dar a luz de forma natural, o caderas que no están a la misma altura.
                    <br><br>
                    Hoy en día muchos problemas de fertilidad diagnosticados por profesionales, 
                    no son como tal. 
                    <br><br>
                    Y por supuesto, consúltanos si tienes menstruaciones dolorosas y desórdenes menstruales.
                    <br><br>
                    <h3><strong>APLICACIONES EN PROBLEMAS HORMONALES:</strong></h3>
