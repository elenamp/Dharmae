                        pero todo tiene un por qué y es lo que quiero explicaros.
                        <br><br>
                        Para qué os deis cuenta del alcance y la historia que tiene esta terapia, 
                        solo os diré que sus comienzos datan del siglo XIX, aunque hay constancia 
                        de prácticas muy parecidas muchos siglos atrás. 
                        En EE.UU, Francia, Inglaterra y Suiza está la cuna de la Osteopatía. En estos países
                        ``ir al Osteópata´´ está a la orden del día, para ellos es como nuestros médicos aquí.
                        <br><br>
                        ¿Y esto realmente por qué no ocurre aquí? Una de mis opciones es porque 
                        las personas no saben cuánto bien hace una sesión de osteopatía.
